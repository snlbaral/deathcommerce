    public function getStates($countryId) {
        $states = State::where('country_id', $countryId)->get()->toArray();
        return response()->json(
            [
                'status'=>true,
                'data'=> $states
            ]
        );
    }

    public function getCities(Request $request, $countryId) {
        $cities = City::where('country_id', $countryId)->whereIn('state_id', $request->state_ids)->get();
        return response()->json(
            [
                'status'=>true,
                'data'=> $cities
            ]
        );
    }
